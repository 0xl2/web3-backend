import { Router } from 'express';
import { UserValidation } from '@/validations';
import { ACCESS_RIGHTS } from '@/config/roles';
import { googleAuth } from '@/middlewares/auth';
import { validate } from '@/middlewares/validate';
import { authApiKey } from '@/middlewares/auth-api-key';
import { UserController } from '@/controllers/admin/user.controller';
import { extractSortAndPagination } from '@/middlewares/sortAndPagination';

const router = Router();

router
  .route('/')
  .post(googleAuth(ACCESS_RIGHTS.MANAGE_USERS), authApiKey, validate(UserValidation.createUser), UserController.createUser)
  .get(googleAuth(ACCESS_RIGHTS.GET_USERS), authApiKey, validate(UserValidation.getUsers), extractSortAndPagination, UserController.getUsers);
router
  .route('/me')
  .get(googleAuth(), authApiKey, validate(UserValidation.getUser), UserController.getLoggedInUser)
router
  .route('/:userId')
  .get(googleAuth(ACCESS_RIGHTS.GET_USERS), authApiKey, validate(UserValidation.getUser), UserController.getUser)
  .patch(googleAuth(ACCESS_RIGHTS.MANAGE_USERS), authApiKey, validate(UserValidation.updateUser), UserController.updateUser)
  .delete(googleAuth(ACCESS_RIGHTS.MANAGE_USERS), authApiKey, validate(UserValidation.deleteUser), UserController.deleteUser);

export const UserRoute = {
  router,
};

